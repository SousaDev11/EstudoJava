package aulas;

public class Condicionais {
    static void main() {

        int salario = 2000;

        if (salario > 5000) {
            System.out.println("Salario muito alto");

        } else if (salario > 3000) {
            System.out.println("Salario alto");

        }else {
            System.out.println("Salario baixo");
        }

    }
}
